#WAP that performs all arithmetic operations on three variables
a = int(input("Enter 1st number - "))
b = int(input("Enter 2nd number - "))
c = int(input("Enter 3rd number - "))
print("Addition operation - "+ str(a+b+c))
print("Substraction operation - "+ str((a-b)-c))
print("Multiplication operation - "+ str(a*b*c))
print("Division operation - "+ str((a//b)//c))
print("Mod operation - "+ str((a%b)%c))